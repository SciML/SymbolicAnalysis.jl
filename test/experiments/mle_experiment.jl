"""
Experiment: Maximum Likelihood Estimation on SPD Matrices

This experiment demonstrates a maximum likelihood estimation problem on the
symmetric positive definite (SPD) manifold. Given n sample covariance matrices,
we estimate the Frechet mean by minimizing the sum of squared geodesic distances:

    minimize  sum_i  d^2(X, S_i)

where d is the Riemannian distance on SPD and S_i are observed covariance matrices.

This is the negative log-likelihood (up to constants) for a Gaussian distribution
on the SPD manifold. The objective is geodesically convex but Euclidean non-convex.

We show that:
1. DGCP correctly verifies the problem as g-convex
2. Standard DCP (Euclidean analysis) cannot verify it as convex
3. Verification is fast even for larger problem sizes

Addresses:
- Reviewer 385: practical application of DGCP verification
- Reviewer 399: comparison between DCP and DGCP on real statistical problems
"""

using SymbolicAnalysis
using Manifolds
using Symbolics
using LinearAlgebra
using Random
using Statistics
using Printf
using Test

#==============================================================================#
# MLE Problem Construction
#==============================================================================#

"""
Generate synthetic sample covariance matrices from a known mean on SPD(n).

Samples are generated by perturbing a true mean along random geodesics,
simulating draws from a distribution concentrated around the Frechet mean.
"""
function generate_spd_samples(n::Int, num_samples::Int, spread::Float64; seed::Int = 42)
    Random.seed!(seed)
    M = SymmetricPositiveDefinite(n)

    # True mean: a random SPD matrix
    A = randn(n, n)
    true_mean = A * A' + I

    # Generate samples by perturbing along random tangent directions
    samples = Matrix{Float64}[]
    for _ = 1:num_samples
        # Random tangent vector (symmetric matrix)
        V = randn(n, n)
        V = (V + V') / 2
        V = spread * V / norm(V)  # Scale by spread

        # Exponential map to get a nearby SPD matrix
        try
            S = exp(Symmetric(log(Symmetric(true_mean)) + V))
            if isposdef(S) && all(isfinite, S)
                push!(samples, Matrix(S))
            else
                # Fallback: simple perturbation
                B = randn(n, n)
                push!(samples, B * B' + I)
            end
        catch
            B = randn(n, n)
            push!(samples, B * B' + I)
        end
    end

    return samples, true_mean
end

#==============================================================================#
# DGCP Verification of MLE Objective
#==============================================================================#

"""
Build and verify the MLE objective symbolically using SymbolicAnalysis.

The objective is: sum_i d^2(X, S_i)
This should be verified as GConvex by DGCP but not as Convex by DCP.
"""
function verify_mle_objective(n::Int, num_samples::Int; verbose::Bool = true)
    # Create symbolic matrix variable
    @variables X[1:n, 1:n]
    M = SymmetricPositiveDefinite(n)

    # Generate sample covariance matrices
    samples, true_mean = generate_spd_samples(n, num_samples, 0.5)

    # Build the symbolic MLE objective: sum of squared distances
    objective = sum(Manifolds.distance(M, S, X)^2 for S in samples)

    # Analyze with DGCP (manifold-aware)
    dgcp_time = @elapsed dgcp_result = analyze(objective, M)

    # Analyze with DCP only (Euclidean, no manifold)
    dcp_time = @elapsed dcp_result = analyze(objective)

    if verbose
        println("  Matrix size: $(n)x$(n)")
        println("  Samples: $(num_samples)")
        println("  DGCP result: gcurvature = $(dgcp_result.gcurvature)")
        println("  DCP result:  curvature  = $(dgcp_result.curvature)")
        println("  DGCP time: $(@sprintf("%.4f", dgcp_time)) s")
        println("  DCP time:  $(@sprintf("%.4f", dcp_time)) s")
    end

    return (
        n = n,
        num_samples = num_samples,
        gcurvature = dgcp_result.gcurvature,
        curvature = dgcp_result.curvature,
        dgcp_time = dgcp_time,
        dcp_time = dcp_time,
        is_gconvex = dgcp_result.gcurvature == SymbolicAnalysis.GConvex ||
                     dgcp_result.gcurvature == SymbolicAnalysis.GLinear,
        is_eucl_convex = dgcp_result.curvature == SymbolicAnalysis.Convex ||
                         dgcp_result.curvature == SymbolicAnalysis.Affine,
    )
end

#==============================================================================#
# Extended MLE Verification: Tyler's M-Estimator
#==============================================================================#

"""
Verify Tyler's M-estimator objective as g-convex.

Tyler's M-estimator finds the MLE of a matrix-variate elliptical distribution:

    minimize  sum_i log(x_i' X^{-1} x_i) + (1/n) logdet(X)

This is g-convex on SPD but not Euclidean convex.
"""
function verify_tyler_mle(n::Int, num_vectors::Int; verbose::Bool = true)
    @variables X[1:n, 1:n]
    M = SymmetricPositiveDefinite(n)

    Random.seed!(123)
    xs = [randn(n) for _ = 1:num_vectors]

    # Tyler's M-estimator objective
    objective =
        sum(SymbolicAnalysis.log_quad_form(x, inv(X)) for x in xs) + (1 / n) * logdet(X)

    # Analyze
    dgcp_time = @elapsed dgcp_result = analyze(objective, M)
    dcp_time = @elapsed dcp_result = analyze(objective)

    if verbose
        println("  Matrix size: $(n)x$(n)")
        println("  Vectors: $(num_vectors)")
        println("  DGCP result: gcurvature = $(dgcp_result.gcurvature)")
        println("  DCP result:  curvature  = $(dgcp_result.curvature)")
        println("  DGCP time: $(@sprintf("%.4f", dgcp_time)) s")
        println("  DCP time:  $(@sprintf("%.4f", dcp_time)) s")
    end

    return (
        n = n,
        num_vectors = num_vectors,
        gcurvature = dgcp_result.gcurvature,
        curvature = dgcp_result.curvature,
        dgcp_time = dgcp_time,
        dcp_time = dcp_time,
        is_gconvex = dgcp_result.gcurvature == SymbolicAnalysis.GConvex ||
                     dgcp_result.gcurvature == SymbolicAnalysis.GLinear,
        is_eucl_convex = dgcp_result.curvature == SymbolicAnalysis.Convex ||
                         dgcp_result.curvature == SymbolicAnalysis.Affine,
    )
end

#==============================================================================#
# Main Experiment
#==============================================================================#

function run_mle_experiment()
    println("="^70)
    println("MLE EXPERIMENT: Maximum Likelihood Estimation on SPD Manifold")
    println("="^70)

    #----------------------------------------------------------------------
    # Part 1: Frechet Mean (sum of squared distances)
    #----------------------------------------------------------------------
    println("\n" * "-"^70)
    println("Part 1: Frechet Mean MLE -- minimize sum_i d^2(X, S_i)")
    println("-"^70)

    frechet_results = []
    configs = [
        (n = 3, m = 3),
        (n = 3, m = 5),
        (n = 3, m = 10),
        (n = 5, m = 3),
        (n = 5, m = 5),
        (n = 5, m = 10),
    ]

    for cfg in configs
        println("\nConfig: n=$(cfg.n), samples=$(cfg.m)")
        result = verify_mle_objective(cfg.n, cfg.m)
        push!(frechet_results, result)
    end

    #----------------------------------------------------------------------
    # Part 2: Tyler's M-Estimator
    #----------------------------------------------------------------------
    println("\n" * "-"^70)
    println("Part 2: Tyler's M-Estimator MLE")
    println("-"^70)

    tyler_results = []
    tyler_configs = [(n = 3, k = 3), (n = 3, k = 5), (n = 5, k = 3), (n = 5, k = 5)]

    for cfg in tyler_configs
        println("\nConfig: n=$(cfg.n), vectors=$(cfg.k)")
        result = verify_tyler_mle(cfg.n, cfg.k)
        push!(tyler_results, result)
    end

    #----------------------------------------------------------------------
    # Summary Table
    #----------------------------------------------------------------------
    println("\n" * "="^70)
    println("SUMMARY TABLE")
    println("="^70)
    println()

    # Frechet mean results
    println("Frechet Mean MLE (sum of squared Riemannian distances):")
    println("-"^70)
    println(
        rpad("Config", 16),
        " | ",
        rpad("G-Convex", 10),
        " | ",
        rpad("Eucl-Convex", 12),
        " | ",
        rpad("DGCP (s)", 10),
        " | ",
        "DCP (s)",
    )
    println("-"^70)

    for r in frechet_results
        println(
            rpad("n=$(r.n), m=$(r.num_samples)", 16),
            " | ",
            rpad(r.is_gconvex ? "YES" : "No", 10),
            " | ",
            rpad(r.is_eucl_convex ? "Yes" : "NO", 12),
            " | ",
            rpad(@sprintf("%.4f", r.dgcp_time), 10),
            " | ",
            @sprintf("%.4f", r.dcp_time)
        )
    end

    println()
    println("Tyler's M-Estimator MLE:")
    println("-"^70)
    println(
        rpad("Config", 16),
        " | ",
        rpad("G-Convex", 10),
        " | ",
        rpad("Eucl-Convex", 12),
        " | ",
        rpad("DGCP (s)", 10),
        " | ",
        "DCP (s)",
    )
    println("-"^70)

    for r in tyler_results
        println(
            rpad("n=$(r.n), k=$(r.num_vectors)", 16),
            " | ",
            rpad(r.is_gconvex ? "YES" : "No", 10),
            " | ",
            rpad(r.is_eucl_convex ? "Yes" : "NO", 12),
            " | ",
            rpad(@sprintf("%.4f", r.dgcp_time), 10),
            " | ",
            @sprintf("%.4f", r.dcp_time)
        )
    end

    #----------------------------------------------------------------------
    # Key Finding
    #----------------------------------------------------------------------
    all_gconvex =
        all(r -> r.is_gconvex, frechet_results) && all(r -> r.is_gconvex, tyler_results)
    none_eucl_convex =
        !any(r -> r.is_eucl_convex, frechet_results) &&
        !any(r -> r.is_eucl_convex, tyler_results)

    println("\n" * "-"^70)
    println("KEY FINDINGS:")
    println("  1. All MLE objectives verified as g-convex by DGCP: $(all_gconvex)")
    println("  2. None verified as Euclidean convex by DCP: $(none_eucl_convex)")
    println("  3. DGCP enables verification of statistical problems on SPD manifolds")
    println("     that are fundamentally beyond the scope of classical DCP.")
    println("-"^70)

    return (frechet = frechet_results, tyler = tyler_results)
end

#==============================================================================#
# Tests
#==============================================================================#

@testset "MLE on SPD Manifold" begin
    @testset "Frechet Mean MLE is g-convex" begin
        result = verify_mle_objective(3, 3; verbose = false)
        @test result.is_gconvex
        @test !result.is_eucl_convex
    end

    @testset "Tyler's M-Estimator MLE is g-convex" begin
        result = verify_tyler_mle(3, 3; verbose = false)
        @test result.is_gconvex
        @test !result.is_eucl_convex
    end

    @testset "Verification scales with problem size" begin
        # Verify that DGCP works across different matrix sizes
        for n in [3, 5]
            result = verify_mle_objective(n, 3; verbose = false)
            @test result.is_gconvex
            @test result.dgcp_time > 0
        end
    end
end

# Run if executed directly
if abspath(PROGRAM_FILE) == @__FILE__
    run_mle_experiment()
end
